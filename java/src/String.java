public class String {
    public static void main(java.lang.String[] args) {
        System.out.println(12+34); //46
        System.out.println("Yun"+"TaeHyeon");
        System.out.println("Yun"+1224);
        System.out.println(1224+"Yun");
        System.out.println("Yun"+1224+1234); //Yun12241234
        System.out.println(1224+1234+"Yun"); //2458Yun
        System.out.println(""+12+24+"Yun"); //1224Yun
    }
}
